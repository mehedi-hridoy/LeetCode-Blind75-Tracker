        let currentProblem = null;

        function login() {
            const enteredUsername = elements.username.value.trim();
            const enteredPassword = elements.password.value.trim();
            const enteredPasswordHash = CryptoJS.SHA256(enteredPassword).toString();

            if (enteredUsername === FIXED_USERNAME && enteredPasswordHash === FIXED_PASSWORD_HASH) {
                elements.loginScreen.classList.add('hidden');
                elements.header.classList.remove('hidden');
                elements.mainContent.classList.remove('hidden');
                elements.footer.classList.remove('hidden');
                elements.cuteFooter.classList.remove('hidden');
                renderTopics();
            } else {
                elements.loginError.classList.remove('hidden');
                setTimeout(() => elements.loginError.classList.add('hidden'), 3000);
            }
        }

        function updateProgress() {
            const total = data.reduce((sum, topic) => sum + topic.problems.length, 0);
            const solved = data.reduce((sum, topic) => sum + topic.problems.filter(p => p.completed).length, 0);
            const percentage = total ? (solved / total) * 100 : 0;
            const circumference = 2 * Math.PI * 70;
            const dashArray = `${(percentage / 100) * circumference} ${circumference}`;

            elements.progressText.textContent = `${solved}/${total} Solved`;
            elements.progressCount.textContent = `${solved}/${total} Problems`;
            elements.progressPercent.textContent = `${Math.round(percentage)}%`;
            elements.progressCircleFill.setAttribute('stroke-dasharray', dashArray);
        }

        function renderTopics() {
            elements.topicsList.innerHTML = data.map(topic => {
                const solved = topic.problems.filter(p => p.completed).length;
                const total = topic.problems.length;
                return `
                    <div class="topic-card bg-white rounded-lg shadow-md p-6 transition-all">
                        <h3 class="text-lg font-semibold text-teal-600 mb-2">${topic.name}</h3>
                        <p class="text-sm text-gray-600 mb-4">${solved}/${total} Solved</p>
                        <div class="space-y-2">
                            ${topic.problems.map(problem => `
                                <div class="flex items-center justify-between text-sm">
                                    <span class="${problem.completed ? 'text-green-500' : 'text-gray-600'} cursor-pointer hover:underline" data-problem-id="${problem.id}">${problem.name}</span>
                                    <i class="fas ${problem.completed ? 'fa-check-circle text-green-500' : 'fa-circle text-gray-400'}"></i>
                                </div>
                            `).join('')}
                        </div>
                    </div>
                `;
            }).join('');
            updateProgress();
        }

        function showToast(message) {
            elements.toastMessage.textContent = message;
            elements.toast.style.opacity = '1';
            setTimeout(() => elements.toast.style.opacity = '0', 3000);
        }

        function showProblemModal(problem) {
            currentProblem = problem;
            elements.modalTitle.textContent = problem.name;
            elements.modalLink.innerHTML = `<a href="${problem.link}" target="_blank" class="text-teal-500 hover:underline">${problem.link}</a>`;
            elements.modalStatus.checked = problem.completed;
            elements.modalBackdrop.classList.remove('hidden');
            elements.modalDone.style.display = 'block'; 
        }

        elements.loginBtn.addEventListener('click', (e) => {
            e.preventDefault();
            login();
        });

        elements.topicsList.addEventListener('click', (e) => {
            const problemSpan = e.target.closest('span[data-problem-id]');
            if (problemSpan) {
                const problemId = problemSpan.dataset.problemId;
                const problem = data.flatMap(t => t.problems).find(p => p.id === problemId);
                if (problem) showProblemModal(problem);
            }
        });

        elements.closeModal.addEventListener('click', () => {
            elements.modalBackdrop.classList.add('hidden');
            currentProblem = null;
        });

        elements.modalDone.addEventListener('click', (e) => {
            e.preventDefault();
            if (currentProblem) {
                currentProblem.completed = elements.modalStatus.checked;
                elements.modalBackdrop.classList.add('hidden');
                renderTopics(); 
                showToast(`${currentProblem.name} updated!`);
                elements.modalDone.style.display = 'block'; 
            }
        });

        elements.saveBtn.addEventListener('click', (e) => {
            e.preventDefault();
            localStorage.setItem('blind75Tracker', JSON.stringify(data));
            showToast('Progress saved successfully!');
        });

